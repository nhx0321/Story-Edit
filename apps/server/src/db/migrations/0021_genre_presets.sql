-- 题材预设迁移
-- 1. 新增题材枚举
-- 2. projects 表新增 genre_tag 列
-- 3. 新增 genre_presets 表
-- 4. 插入 42 条题材预设（14 题材 × 3 Agent）

-- 1. 新增题材枚举
CREATE TYPE genre AS ENUM (
  'xianxia', 'urban', 'apocalypse', 'romance', 'military',
  'political', 'scifi', 'suspense', 'fantasy', 'historical',
  'game', 'male_oriented', 'female_oriented', 'other'
);

-- 2. projects 表新增 genre_tag 列
ALTER TABLE projects ADD COLUMN IF NOT EXISTS genre_tag genre;

-- 3. 新增 genre_presets 表
CREATE TABLE genre_presets (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  genre genre NOT NULL,
  agent_role VARCHAR(50) NOT NULL,
  system_prompt TEXT NOT NULL,
  description TEXT,
  sort_order INTEGER DEFAULT 0,
  created_at TIMESTAMP DEFAULT NOW() NOT NULL,
  updated_at TIMESTAMP DEFAULT NOW() NOT NULL
);

-- 4. 插入各题材预设提示词
-- 每个题材 3 个 Agent：editor（文学编辑）、setting_editor（设定编辑）、writer（小说作者）

