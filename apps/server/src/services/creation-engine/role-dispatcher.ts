import { db } from '../../db';
import { aiRoles } from '../../db/schema';
import { eq, and } from 'drizzle-orm';

export interface ResolvedRole {
  id: string;
  name: string;
  role: string;
  systemPrompt: string;
}

// 默认角色提示词模板（非对话模式使用）
export const DEFAULT_PROMPTS: Record<string, string> = {
  editor: `你是一位经验丰富的文学编辑。你的职责是：
- 构思引人入胜的故事大纲和剧情走向
- 设计合理的叙事结构和节奏
- 确保故事的逻辑性和吸引力
- 为每个章节规划明确的目标和爽点`,

  writer: `你是一位才华横溢的正文作者。你的职责是：
- 根据任务书撰写高质量的章节正文
- 保持文风一致，语言生动
- 注重场景描写、人物对话和情感表达
- 确保与前文的自然衔接`,

  setting_editor: `你是一位严谨的设定编辑。你的职责是：
- 搭建完整的世界观和设定体系
- 确保所有设定的内部一致性
- 校验正文中的设定引用是否准确
- 维护角色状态、物资清单、伏笔追踪`,
};

export async function resolveRole(projectId: string, roleKey: string): Promise<ResolvedRole> {
  const [customRole] = await db.select().from(aiRoles)
    .where(and(eq(aiRoles.projectId, projectId), eq(aiRoles.role, roleKey)));

  if (customRole) {
    return {
      id: customRole.id,
      name: customRole.name,
      role: customRole.role,
      systemPrompt: customRole.systemPrompt || DEFAULT_PROMPTS[roleKey] || '',
    };
  }

  return {
    id: 'default',
    name: roleKey,
    role: roleKey,
    systemPrompt: DEFAULT_PROMPTS[roleKey] || '',
  };
}

